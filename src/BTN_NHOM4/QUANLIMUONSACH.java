    private void btnTraSachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTraSachActionPerformed
        try{
            if(QUANLITHUVIEN.dsmuonsach.Tim(tableMuonSach.getValueAt(tableMuonSach.getSelectedRow(), 0).toString()).isDaTra() == false){
                Date homNay = new Date();
                String Masachtruyen = tableMuonSach.getValueAt(tableMuonSach.getSelectedRow(), 2).toString();
                QUANLITHUVIEN.dsmuonsach.Tim(tableMuonSach.getValueAt(tableMuonSach.getSelectedRow(), 0).toString()).setDaTra(true);
                QUANLITHUVIEN.dsmuonsach.Tim(tableMuonSach.getValueAt(tableMuonSach.getSelectedRow(), 0).toString()).setNgayTra(homNay);
                QUANLITHUVIEN.dssachtruyen.Tim(Masachtruyen).setSoLuong(QUANLITHUVIEN.dssachtruyen.Tim(Masachtruyen).getSoLuong()+1);
                tableModel.fireTableDataChanged();
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Sách này đã được trả rồi", "Lỗi", 1);
            }
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(rootPane, "Vui lòng chỉ định một đối tượng trong Danh sách mượn sách", "Lỗi", 1);
        }
    }//GEN-LAST:event_btnTraSachActionPerformed


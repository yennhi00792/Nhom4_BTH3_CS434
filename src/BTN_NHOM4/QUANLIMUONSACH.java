    private void Them(){
        if(QUANLITHUVIEN.dsthethuvien.Tim(txtMaThe.getText()) != null){
            if(QUANLITHUVIEN.dssachtruyen.Tim(txtMaSachTruyen.getText()) != null){
                SACHTRUYEN sach = QUANLITHUVIEN.dssachtruyen.Tim(txtMaSachTruyen.getText());
                if(sach.getSoLuong() > 0){
                    Date homNay = new Date();
                    randomID++;
                    MUONSACH ms = new MUONSACH("MS0"+Integer.toString(randomID), txtMaThe.getText().toUpperCase(), txtMaSachTruyen.getText().toUpperCase(), homNay, null, false);
                    QUANLITHUVIEN.dsmuonsach.Them(ms);
                    sach.setSoLuong(sach.getSoLuong()-1);
                }
                else{
                    JOptionPane.showMessageDialog(rootPane, "Số sách trong kho đã hết", "Lỗi", 1);
                }
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Mã sách không tồn tại", "Lỗi", 1);
                txtMaSachTruyen.requestFocus();
            }
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Mã thẻ không tồn tại", "Lỗi", 1);
            txtMaThe.requestFocus();
        }
    }
    
